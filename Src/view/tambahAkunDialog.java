    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
    try {
        // Ambil data dari form
        String nama = jTextField1.getText().trim();
        String username = jTextField4.getText().trim();
        String password = new String(jPasswordField1.getPassword()).trim();
        String rtRwText = jTextField2.getText().trim(); // format: "05,02" atau "05"
        String alamat = jTextField3.getText().trim();

        // ==== VALIDASI UMUM ====
        if (nama.isEmpty() || username.isEmpty() || password.isEmpty() || rtRwText.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Semua kolom wajib diisi, kecuali RW (boleh kosong).",
                "Validasi", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // ==== VALIDASI USERNAME ====
        if (!username.matches("^[a-zA-Z0-9_]+$")) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Username hanya boleh mengandung huruf, angka, dan underscore (_).",
                "Format Tidak Valid", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // ==== VALIDASI PASSWORD ====
        if (password.length() < 5) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Password minimal 5 karakter.",
                "Format Tidak Valid", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

                // ==== VALIDASI RT & RW ====
         String rawRT_RW = rtRwText.trim();

        // Split berdasarkan slash (RT / RW)
        String[] parts = rawRT_RW.split("/");

        // ---- VALIDASI RT ----
        String rawRt = parts[0].trim();
        String noRt = rawRt.replaceAll("[^0-9]", ""); // hanya ambil angka

        if (noRt.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Nomor RT wajib diisi dan harus berupa angka.",
                "Format Tidak Valid", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // ---- VALIDASI RW (opsional) ----
        String rw = null;
        ketuaRt data = new ketuaRt();
        if (parts.length > 1) {
            String rawRw = parts[1].trim();
            rw = rawRw.replaceAll("[^0-9]", "");

            if (!rw.isEmpty() && !rw.matches("\\d+")) {
                JOptionPane.showMessageDialog(this,
                    "Nomor RW harus berupa angka jika diisi.",
                    "Format Tidak Valid", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
        

        // RW boleh kosong â†’ set null
        data.setRw((rw != null && !rw.isEmpty()) ? rw : null);

        // Alamat boleh kosong
        data.setAlamat(jTextField3.getText().trim());

        // Debug print untuk memastikan benar
        System.out.println("ISI TEXTBOX RT/RW SEBENARNYA: " + rtRwText);
        System.out.println("RT = " + noRt);
        System.out.println("RW = " + (rw != null && !rw.isEmpty() ? rw : "(kosong)"));

        // Set ke entity (String sesuai DB)
        data.setNoRt(noRt);
        data.setRw((rw != null && !rw.isEmpty()) ? rw : null);

        // ==== VALIDASI NAMA ====
        if (!nama.matches("^[a-zA-Z\\s]+$")) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Nama hanya boleh berisi huruf dan spasi.",
                "Format Tidak Valid", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // ==== BUAT OBJEK ENTITY ====
        data.setNamaUsers(nama);
        data.setUsername(username);
        data.setPassword(password);
        data.setNoRt(noRt);
        data.setRw(rw);
        data.setAlamat(alamat);

        // ==== SIMPAN KE DATABASE ====
        boolean success = controller.KetuaRtController.tambah(data);


        if (success) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Akun berhasil ditambahkan.",
                "Sukses", javax.swing.JOptionPane.INFORMATION_MESSAGE);

            // Kosongkan field setelah berhasil
            jTextField1.setText("");
            jTextField2.setText("");
            jPasswordField1.setText("");
            jTextField3.setText("");
            jTextField4.setText("");

            doClose(RET_OK);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Gagal menambah akun.",
                "Kesalahan", javax.swing.JOptionPane.ERROR_MESSAGE);
        }

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Terjadi kesalahan: " + e.getMessage(),
            "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }


    }//GEN-LAST:event_okButtonActionPerformed

