    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String username = jUsername.getText();
    String password = new String(jPasswordField1.getPassword());

    controller.LoginController loginController = new controller.LoginController();
    controller.LoginController.LoginRole role = loginController.autentikasi(username, password);

switch (role) {
    case ADMIN -> {
        // Ambil data admin dari DAO
        model.dao.adminDao adminDao = new model.dao.adminDao();
        model.entity.admin adminLogin = adminDao.getAdminByUsername(username);

        if (adminLogin != null) {
            // Simpan wilayah ke sesi login
            LoginSession.setWilayah(adminLogin.getWilayah());
        }

        LoginBerhasil popup = new LoginBerhasil(this, true);
        popup.setLocationRelativeTo(this);
        popup.setVisible(true);
        new dashboard().setVisible(true);
        dispose();
    }

    case KETUA_RT -> {
        // Ambil data ketua RT dari DAO
        model.dao.ketuaRtDao rtDao = new model.dao.ketuaRtDao();
        model.entity.ketuaRt rtLogin = rtDao.getKetuaRTByUsername(username);

        if (rtLogin != null) {
            // Simpan wilayah atau alamat sesuai kebutuhan
            LoginSession.setWilayah(rtLogin.getAlamat());
        }

        LoginBerhasil popup = new LoginBerhasil(this, true);
        popup.setLocationRelativeTo(this);
        popup.setVisible(true);
        new homePage().setVisible(true);
        dispose();
    }

    default -> 
        JOptionPane.showMessageDialog(this, "Username atau password salah!", 
                                      "Login Gagal", JOptionPane.ERROR_MESSAGE);
}



    }//GEN-LAST:event_jButton1ActionPerformed

